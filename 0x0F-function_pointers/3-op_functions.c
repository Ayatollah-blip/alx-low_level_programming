#include "3-calc.h"
/**
* op_add - function that adds two integers
*
* @a: param 1
* @b: param 2
*
* Return: sum of a and b
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - function that subs two integers
*
* @a: param 1
* @b: param 2
*
* Return: sub of a and b
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - function that mutiply two integers
*
* @a: param 1
* @b: param 2
*
* Return: multiplication  of a and b
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - function that divides two integers
*
* @a: param 1
* @b: param 2
*
* Return: division of a and b
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - function that divides two integers
*
* @a: param 1
* @b: param 2
*
* Return: division of a and b
*/
int op_mod(int a, int b)
{
return (a % b);
}

